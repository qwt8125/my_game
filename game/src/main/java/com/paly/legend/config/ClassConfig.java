package com.paly.legend.config;

public class ClassConfig {

    private String id;
    private String name;
    private String description;
    private int hp;
    private int attack;
    private int defense;
    private int attackSpeed;
    private int hpPerLevel;
    private int attackPerLevel;
    private int defensePerLevel;
    private int attackSpeedPerLevel;

    public String getId() { return id; }
    public void setId(String id) { this.id = id; }
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }
    public String getDescription() { return description; }
    public void setDescription(String description) { this.description = description; }
    public int getHp() { return hp; }
    public void setHp(int hp) { this.hp = hp; }
    public int getAttack() { return attack; }
    public void setAttack(int attack) { this.attack = attack; }
    public int getDefense() { return defense; }
    public void setDefense(int defense) { this.defense = defense; }
    public int getAttackSpeed() { return attackSpeed; }
    public void setAttackSpeed(int attackSpeed) { this.attackSpeed = attackSpeed; }
    public int getHpPerLevel() { return hpPerLevel; }
    public void setHpPerLevel(int hpPerLevel) { this.hpPerLevel = hpPerLevel; }
    public int getAttackPerLevel() { return attackPerLevel; }
    public void setAttackPerLevel(int attackPerLevel) { this.attackPerLevel = attackPerLevel; }
    public int getDefensePerLevel() { return defensePerLevel; }
    public void setDefensePerLevel(int defensePerLevel) { this.defensePerLevel = defensePerLevel; }
    public int getAttackSpeedPerLevel() { return attackSpeedPerLevel; }
    public void setAttackSpeedPerLevel(int attackSpeedPerLevel) { this.attackSpeedPerLevel = attackSpeedPerLevel; }
}
